package entities;

public class Product {
	
	private String name;
	private double price;
	private int quantity;

	/*
	 * 1º) O comando public public Product(String name, double price, int
	 * quantity){} é um contrutor que obriga a inserção dos campos no instanciamento
	 * de objetos
	 */
	public Product() {
	}

	public Product(String name, double price, int quantity) {
		this.name = name;
		this.price = price;
		this.quantity = quantity;
		
	/*2º O comando this garante que a variável está referenciando os próprios parâmetros do objeto (public String name;
		*public double price;public int quantity)
	 * É possível realizar a SOBRECARGA de contrutores, adicionando mais um construtor com parâmetros diferentes. Conforme exemplo abaixo:
	 */
	}
		
	public Product(String name, double price) {
		this.name = name;
		this.price = price;
	}
	
	//O constrotor padrão [Ex.: Product()], sempre retorna os valores vazios: null para var e string, 0 para números
	//De acordo com o padrão JavaBeans, as variáveis não devem ser acessadas diretamente pelo usuário
	//Para isso são necessários métodos, tais como getters e setters. Estes devem ser posicionados abaixo dos contrutores
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public int getQuantity() {
		return quantity;
	}

	public double totalValueInStock() {
		return price * quantity;
	}

	public void addProducts(int quantity) {
		this.quantity += quantity;
	}

	public void removeProducts(int quantity) {
		this.quantity -= quantity;
	}

	public String toString() {
		return name + ", $ " + String.format("%.2f", price) + ", " + quantity + " units, Total: $ "
				+ String.format("%.2f", totalValueInStock());
	}
}